const createInterstBlock = (interstName, interestPara, interestImg) => {
    let div = document.createElement('div');
    let collage = document.createElement('div');
    let captionGroup = document.createElement('div');

    //add interest group attributes
    div.className = "interest reveal";

    //add collage attributes
    collage.className = "collage";
    let collageImg = document.createElement('img');
    collageImg.src =  interestImg;

    //add caption group attributes
    captionGroup.className = "caption-group";
    let interestBlurb = document.createElement('p');
    interestBlurb.className = "about-me";
    interestBlurb.innerHTML = interestPara;

    document.getElementById("main").appendChild(div);

    div.appendChild(collage);
    collage.appendChild(collageImg);

    div.appendChild(captionGroup);
    captionGroup.appendChild(interestBlurb);
}

//src = (https://alvarotrigo.com/blog/css-animations-scroll/)
const reveal = () => {
    var reveals = document.querySelectorAll(".reveal");
    for (var i = 0; i < reveals.length; i++) {
      var windowHeight = window.innerHeight;
      var elementTop = reveals[i].getBoundingClientRect().top;
      var elementVisible = 150;
      if (elementTop < windowHeight - elementVisible) {
        reveals[i].classList.add("active");
      } else {
        reveals[i].classList.remove("active");
      }
    }
  }

  window.addEventListener("scroll", reveal);

  // To check the scroll position on page load
  reveal();
